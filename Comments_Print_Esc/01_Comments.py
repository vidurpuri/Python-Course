# We can write comments in Python using the hash symbol (#).
# Comments are ignored by the Python interpreter and are used to explain the code.

# Multi-line comments can be written using triple quotes (''' or """).
# These comments can span multiple lines and are also ignored by the interpreter.

'''This is a multi-line comment.
It can be used to provide detailed explanations or documentation for the code.
Hi 
I am a comment.
This is a multi-line comment.
'''