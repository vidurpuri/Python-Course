# String Methods
name = 'Vidur Puri'
print(len(name))
print(name.upper())  # VIDUR PURI
print(name.lower())  # vidur puri
print(name.title())  # Vidur Puri
print(name.strip())  # Vidur Puri
print(name.find('i'))  # 1
print(name.replace('V', 'A'))  # Aidur Puri
print(name.split('i'))  # ['V', 'dur Pur', '']

