'''
\n = Newline character
\t = Tab character
\\ = Backslash character
\' = Single quote character 
\" = Double quote character
'''
# Escape characters in Python are used to insert characters that are illegal in a string.
# For example, if you want to include a newline or a tab in a string, you  use escape characters.

print("Hello\nWorld")  # \n is used to insert a newline
print("Hello\tWorld")  # \t is used to insert a tab 
print("Hello\\World")  # \\ is used to insert a backslash
print("Hello\'World")  # \' is used to insert a single quote
print("Hello\"World")  # \" is used to insert a double quote