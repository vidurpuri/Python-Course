for i in range(1,5): # range(start, stop) generates numbers from start to stop-1
    print("Iteration:", i)

# The range function can also take a step value
for i in range(1, 10, 2): # range(start, stop, step) generates numbers from start to stop-1, incrementing by step
    print("Iteration:", i)

# print table for 5
for i in range(1,11):
    print(" 5 X",i,"=",5*i)
